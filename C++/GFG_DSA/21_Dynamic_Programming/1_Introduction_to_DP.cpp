// Dynamic Programming is mainly an 
// optimization over plain recursion

// The idea is to simply store the results 
// of subproblems, so that we do not have 
// to re-compute them when needed later. 
// This simple optimization reduces time complexities

// There are two ways to implement Dynamic Programming :
// 1) Memoization (Top Down)
// 2) Tabulation (Bottom Up)

// Applications ::
// Bellman Ford Algorithm 
// Floyd Warshall Algorithm
// Difference Utity (LCS)
// Search Closed Words (Edit Disance)
// Resource Allocation (0-1 Knapsack)
